//Laço For

console.log("-----------Início do For-----------");

for (let i=0; i<10; i++){
    console.log("Valor de i=" + i);
}

console.log("-----------Fim do For-----------");

//Laço While

console.log("-----------Início do While-----------");

let i=0;
while (i<10){
    console.log("Valor de i=" + i++);
}

console.log("-----------Fim do While-----------");

//Laço Do...While

console.log("-----------Início do Laço Do...While-----------");

i=0;
do {
    console.log("Valor de i=" + i++);
} while(i<10);

console.log("-----------Fim do Laço Do...While-----------");